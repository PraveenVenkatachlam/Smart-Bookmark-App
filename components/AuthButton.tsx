"use client";

import { createClient } from "@/lib/supabase/client";
import { useRouter } from "next/navigation";
import { useState } from "react";

// Renders "Sign in with Google" on /login, and a sign-out
// button on /dashboard.  It's a client component because
// it triggers browser-side OAuth and navigation.

export function AuthButton({ showSignOut = false }: { showSignOut?: boolean }) {
  const supabase = createClient();
  const router = useRouter();
  const [loading, setLoading] = useState(false);

  const handleSignIn = async () => {
    setLoading(true);
    const { error } = await supabase.auth.signInWithOAuth({
      provider: "google",
      options: {
        // After Google authenticates, redirect here:
        redirectTo: `${window.location.origin}/auth/callback`,
      },
    });
    if (error) {
      console.error("OAuth error:", error.message);
      setLoading(false);
    }
    // The browser will navigate to Google's consent screen,
    // so we don't need to setLoading(false) on success.
  };

  const handleSignOut = async () => {
    setLoading(true);
    await supabase.auth.signOut();
    router.push("/login");
    router.refresh();        // clear server-side cached session
  };

  if (showSignOut) {
    return (
      <button
        onClick={handleSignOut}
        disabled={loading}
        className="rounded-lg bg-gray-800 px-4 py-2 text-sm font-medium
                   text-gray-300 transition hover:bg-gray-700
                   disabled:opacity-50"
      >
        {loading ? "Signing out…" : "Sign Out"}
      </button>
    );
  }

  return (
    <button
      onClick={handleSignIn}
      disabled={loading}
      className="flex w-full items-center justify-center gap-3 rounded-lg
                 bg-white px-4 py-3 text-sm font-semibold text-gray-900
                 transition hover:bg-gray-100 disabled:opacity-50"
    >
      {/* Google "G" SVG icon */}
      <svg className="h-5 w-5" viewBox="0 0 24 24">
        <path
          d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06
             5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74
             3.28-8.1z"
          fill="#4285F4"
        />
        <path
          d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66
             -2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18
             v2.84C3.99 20.53 7.7 23 12 23z"
          fill="#34A853"
        />
        <path
          d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35
             -2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18
             4.93l2.85-2.22.81-.62z"
          fill="#FBBC05"
        />
        <path
          d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45
             2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66
             2.84c.87-2.6 3.3-4.53 6.16-4.53z"
          fill="#EA4335"
        />
      </svg>
      {loading ? "Redirecting…" : "Sign in with Google"}
    </button>
  );
}